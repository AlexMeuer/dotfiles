-- Pad each line to max display width with Braille blank (⠀) so snacks' per-line
-- centering preserves the art's internal alignment instead of wobbling.
local function block(lines)
  local max = 0
  for _, l in ipairs(lines) do
    local w = vim.fn.strdisplaywidth(l)
    if w > max then max = w end
  end
  local out = {}
  for _, l in ipairs(lines) do
    out[#out + 1] = l .. string.rep("⠀", max - vim.fn.strdisplaywidth(l))
  end
  return table.concat(out, "\n")
end

